/* ── Рендер панели ── */
// keys — строка или массив строк, например 'tree' или ['tree', 'calculation']
function buildHelpPanel(keys) {
  if (!Array.isArray(keys)) keys = [keys];

  // Собираем все валидные разделы
  const sections = [];
  keys.forEach(key => {
    const data = HELP_CONTENT[key];
    if (!data) return;
    // Если разделов несколько — добавляем разделитель с названием раздела
    if (keys.length > 1) {
      sections.push({ _groupTitle: data.title });
    }
    data.sections.forEach(sec => sections.push(sec));
  });

  if (!sections.length) return;

  const panel = document.createElement('div');
  panel.className = 'help-panel';
  panel.id = 'helpPanel';

  // Заголовок панели: если один раздел — его название, если несколько — «Справка»
  const panelTitle = keys.length === 1
    ? HELP_CONTENT[keys[0]].title
    : 'Справка';

  let html = `<div class="help-panel-title">
    <svg width="18" height="18" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="2">
      <path stroke-linecap="round" stroke-linejoin="round" d="M13 16h-1v-4h-1m1-4h.01M12 2a10 10 0 100 20A10 10 0 0012 2z"/>
    </svg>
    ${panelTitle}
  </div>`;

  sections.forEach((sec, i) => {
    // Разделитель-заголовок группы
    if (sec._groupTitle) {
      html += `<div class="help-group-title">${sec._groupTitle}</div>`;
      return;
    }
    html += `
      <div class="instr-sub-card">
        <button class="instr-sub-header" onclick="helpToggleSub(this)">
          <svg class="instr-sub-chevron" xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke="currentColor" stroke-width="2.5">
            <path stroke-linecap="round" stroke-linejoin="round" d="M9 5l7 7-7 7"/>
          </svg>
          ${sec.label}
        </button>
        <div class="instr-sub-body" style="display:none;">${sec.html}</div>
      </div>`;
  });

  panel.innerHTML = html;
  document.body.appendChild(panel);
}

function helpToggleSub(btn) {
  const body    = btn.nextElementSibling;
  const chevron = btn.querySelector('.instr-sub-chevron');
  const isOpen  = body.style.display === 'block';
  body.style.display      = isOpen ? 'none' : 'block';
  chevron.style.transform = isOpen ? 'rotate(0deg)' : 'rotate(90deg)';
}

/* ── Инициализация ── */
document.addEventListener('DOMContentLoaded', () => {
  const raw = document.body.dataset.help;
  if (!raw) return;

  // Поддержка нескольких ключей через запятую: data-help="tree,calculation"
  const keys = raw.split(',').map(k => k.trim()).filter(k => HELP_CONTENT[k]);
  if (!keys.length) return;

  /* Кнопка ? */
  const btn = document.createElement('button');
  btn.className   = 'help-btn';
  btn.title       = 'Справка';
  btn.textContent = '?';
  document.body.appendChild(btn);

  /* Панель */
  buildHelpPanel(keys);
  const panel = document.getElementById('helpPanel');

  /* Переключение по клику */
  btn.addEventListener('click', (e) => {
    e.stopPropagation();
    panel.classList.toggle('help-panel--open');
  });

  /* Закрытие по клику вне панели */
  document.addEventListener('click', (e) => {
    if (!panel.contains(e.target) && e.target !== btn) {
      panel.classList.remove('help-panel--open');
    }
  });
});
